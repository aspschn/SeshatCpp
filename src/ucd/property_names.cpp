PropertyValueName property_value_name(bool val)
{
    PropertyValueName result = {};
    if (val == true) {
        result.full = "Yes";
        result.abbr = "Y";
        result.alt[0] = "T";
        result.alt[1] = "True";
    } else {
        result.full = "No";
        result.abbr = "N";
        result.alt[0] = "F";
        result.alt[1] = "False";
    }
    return result;
}


